      <p class="muted">Majtikinixmatikan in tajpalolmej wan in tanawatimej</p>
      
<p>Tech nijin tanextilis moneki majtikinixmatikan in tajpalolmej wan in tanawatilmej ijkuak yowi se masewal wan noijkon ijkuak yowi okse iwan. Kijtosneki, tajpalolmej wan tanawatilismej ninselti wan ijkuak miakej. Moneki majtikmatikan ken se motajpalowa ika náwat ijkuak se kinkalpanowilia in masewal altepemej.</p>
<p class="muted">En esta lección se pretende dar a conocer las diferentes maneras en las que una persona puede despedirse de otra y cuando esa persona va acompañada. Es decir, saludos y despedidas en singular y plural. Es necesario saber cómo saludar en lengua náhuatl cuando se visitan pueblos originarios.</p>

